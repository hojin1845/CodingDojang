#나눗셈 후 나머지를 구하는 % 연산자 
#나눗셈 후 나누어 떨어지지 않을 때 나머지를 구해보자
#5 % 2 = 1
#5를 2로 나누면 두 번 나눌수 있고 1이 남음
#%는 두 수를 나누었을 때 나머지만 구하며 모튤로 연산자 라고 부름
#몫은 버림 나눗셈(//)으로 구할수 있음

