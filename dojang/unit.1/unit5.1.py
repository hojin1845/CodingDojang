#파이썬 3에서 정수끼리 나눗셈 결과가 정수로 나도록 만들어 보자
#//로 나눗셈을 하면 됨

#//은 버림 나눗셈이라고 불리며 나눗셈의 결과에서 소수점 이하는 버림
#실수에 // 연산자를 사용하면 결과는 실수가 나오며 소수점 이하는 버림
